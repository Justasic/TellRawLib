package net.derpz.tellrawlib.core.enums;

/**
 * A representation of all the Bukkit color codes.
 * @see <a href="https://wiki.ess3.net/mc/">Essentials Color Code Reference</a>
 */
public enum Color {
    /**
     * Represents &b in Bukkit chat color code notation
     */
    AQUA,
    /**
     * Represents &0 in Bukkit chat color code notation
     */
    BLACK,
    /**
     * Represents &9 in Bukkit chat color code notation
     */
    BLUE,
    /**
     * Represents &3 in Bukkit chat color code notation
     */
    DARK_AQUA,
    /**
     * Represents &1 in Bukkit chat color code notation
     */
    DARK_BLUE,
    /**
     * Represents &8 in Bukkit chat color code notation
     */
    DARK_GRAY,
    /**
     * Represents &5 in Bukkit chat color code notation
     */
    DARK_PURPLE,
    /**
     * Represents &4 in Bukkit chat color code notation
     */
    DARK_RED,
    /**
     * Represents &6 in Bukkit chat color code notation
     */
    GOLD,
    /**
     * Represents &7 in Bukkit chat color code notation
     */
    GRAY,
    /**
     * Represents &a in Bukkit chat color code notation
     */
    GREEN,
    /**
     * Represents &d in Bukkit chat color code notation
     */
    LIGHT_PURPLE,
    /**
     * Represents &c in Bukkit chat color code notation
     */
    RED,
    /**
     * Represents &f in Bukkit chat color code notation
     */
    WHITE,
    /**
     * Represents &e in Bukkit chat color code notation
     */
    YELLOW
}
